def ordered_columns(columns: Sequence[tuple[str, str]]) -> List[str]:
    return [name for name, _ in columns]


def normalize_row(row: Dict[str, Any], columns: Sequence[tuple[str, str]]) -> Dict[str, Any]:
    known = {name for name, _ in columns}
    unknown = sorted(str(key) for key in row.keys() if key not in known)
    if unknown:
        raise KeyError(f"Unknown row fields: {', '.join(unknown)}")
    out: Dict[str, Any] = {}
    for name, _ in columns:
        out[name] = row.get(name)
    return out


def normalize_rows(rows: Iterable[Dict[str, Any]], columns: Sequence[tuple[str, str]]) -> List[Dict[str, Any]]:
    return [normalize_row(row, columns) for row in rows]
